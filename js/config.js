/* ===============================
   보드(Board) 설정
   =============================== */

// 가로 칸 수 (표준 테트리스: 10)
const BOARD_COLS = 10;

// 세로 칸 수 (표준 테트리스: 20)
const BOARD_ROWS = 20;


/* ===============================
   블록(Block) 설정
   =============================== */

// 한 칸(블록)의 픽셀 크기
// canvas 크기와 직접적으로 연결됨
const BLOCK_SIZE = 30;

// 블록 테두리 두께
const BLOCK_BORDER_WIDTH = 1;


/* ===============================
   게임 속도 설정
   =============================== */

// 기본 낙하 속도 (ms 단위)
// 값이 작을수록 블록이 빠르게 떨어짐
const DROP_INTERVAL = 1000;


/* ===============================
   색상(Color) 설정
   =============================== */

// 0은 빈 칸 (렌더링하지 않음)
const COLORS = [
   null,
   "#00f0f0", // I
   "#f0f000", // O
   "#a000f0", // T
   "#00f000", // S
   "#f00000", // Z
   "#0000f0", // J
   "#ef8b00"  // L
];


/* ===============================
   캔버스(Canvas) 설정
   =============================== */

// canvas 가로 길이 = 블록 가로 수 * 블록 크기
const CANVAS_WIDTH = BOARD_COLS * BLOCK_SIZE;

// canvas 세로 길이 = 블록 세로 수 * 블록 크기
const CANVAS_HEIGHT = BOARD_ROWS * BLOCK_SIZE;

